package com.treetop.businessobjects;

/**
 * @author thaile
 *
 * General Descriptive Code file for Dried System.
 */
public class DescriptiveCodeDried {
	
	String	descType	= "";
	String	descCode	= "";		
	String	descFull 	= "";
	String	descShort	= "";
	String	plantNumber	= "";
	String	productLine	= "";

	
	/**
	 * @return Returns the descCode.
	 */
	public String getDescCode() {
		return descCode;
	}
	/**
	 * @param descCode The descCode to set.
	 */
	public void setDescCode(String descCode) {
		this.descCode = descCode;
	}
	/**
	 * @return Returns the descFull.
	 */
	public String getDescFull() {
		return descFull;
	}
	/**
	 * @param descFull The descFull to set.
	 */
	public void setDescFull(String descFull) {
		this.descFull = descFull;
	}
	/**
	 * @return Returns the descShort.
	 */
	public String getDescShort() {
		return descShort;
	}
	/**
	 * @param descShort The descShort to set.
	 */
	public void setDescShort(String descShort) {
		this.descShort = descShort;
	}
	/**
	 * @return Returns the descType.
	 */
	public String getDescType() {
		return descType;
	}
	/**
	 * @param descType The descType to set.
	 */
	public void setDescType(String descType) {
		this.descType = descType;
	}
	/**
	 * @return Returns the plantNumber.
	 */
	public String getPlantNumber() {
		return plantNumber;
	}
	/**
	 * @param plantNumber The plantNumber to set.
	 */
	public void setPlantNumber(String plantNumber) {
		this.plantNumber = plantNumber;
	}
	/**
	 * @return Returns the productLine.
	 */
	public String getProductLine() {
		return productLine;
	}
	/**
	 * @param productLine The productLine to set.
	 */
	public void setProductLine(String productLine) {
		this.productLine = productLine;
	}
}
